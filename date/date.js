export function day(day){
  let todayDay
  switch(day){
    case 0:
      todayDay = 'Sun'
      break
    case 1:
      todayDay = 'Mon'
      break
    case 2:
      todayDay = 'Tue'
      break
    case 3:
      todayDay = 'Wed'
      break
    case 4:
      todayDay = 'Thu'
      break
    case 5:
      todayDay = 'Fri'
      break
    case 6:
      todayDay = 'Sat'
      break
  }
  return todayDay
}

